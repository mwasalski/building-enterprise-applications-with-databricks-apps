# MAGIC ## Building Enterprise Applications with Databricks Apps
# MAGIC
# MAGIC This course introduces Databricks Apps, a new feature that empowers developers to build and deploy secure data and AI applications directly on the Databricks platform. It is primarily designed for data scientists, data engineers, and developers who need to create custom applications that make data insights accessible to non-technical users within their organizations.
# MAGIC
# MAGIC The course begins with an overview of the main components of Databricks Apps, followed by the deployment of a simple app using available templates. Next, you will learn how to build and deploy a data-driven application that integrates with other platform features. The course then covers how to set up an IDE for Databricks Apps, and how to edit and deploy an app using an IDE. It concludes with how to implement security and best practices for an enterprise app.
# MAGIC
# MAGIC ## Prerequisites
# MAGIC
# MAGIC - Foundational Knowledge of Databricks Platform
# MAGIC - Understanding of the core Databricks components (workspaces, clusters, SQL warehouses, Unity Catalog, Jobs, Model Serving)
